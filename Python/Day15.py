import time
a = time.strftime('%H:%M:%S')
print(a)
b = time.strftime('%H')
print(b)
c = time.strftime('%M')
print(c)
d = time.strftime('%S')
print(d)
d = time.strftime('%a')
print(d)
d = time.strftime('%A')
print(d)
d = time.strftime('%B') # b for half month name
print(d)
d = time.strftime('%c')
print(d)
d = time.strftime('%I') #Hour (12-hour clock)
print(d)
d = time.strftime('%j') # Day of the year
print(d)
d = time.strftime('%p') #AM or PM
print(d)
d = time.strftime('%U') # week number of the year
print(d)
d = time.strftime('%w') # shows day of the week in no. (0 as sunday to 6)
print(d)